    private void botaoGotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoGotoActionPerformed
        // TODO add your handling code here:
        try {
            int i = Integer.parseInt(fieldGoto.getText());
            if (i <= estados.size() && i >= 0) {
                indice = i;
                estado = estados.get(indice);
                setarTextos(estado.quadrados);
                checarBotoes();
                fieldGoto.setText(indice + "");
            }
        } catch (NumberFormatException e) {
            setarEstado(estados.size() - 1);
        }
    }//GEN-LAST:event_botaoGotoActionPerformed
