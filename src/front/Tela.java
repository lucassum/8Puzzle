    private void botaoResolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoResolverActionPerformed
        // TODO add your handling code here:
        botaoResolver.setEnabled(false);
        if (Algoritmos.isPossivel(estado)) {
            Estado inicial = estado;
            Algoritmos.resolver(estado, this);
            while (estado.estadoAnterior != null) {
                estados.add(estado);
                estado = estado.estadoAnterior;
            }
            if (!estados.isEmpty()) {
                estados.add(inicial);
                Collections.reverse(estados);
                estado = estados.get(estados.size() - 1);
                indice = estados.indexOf(estado);
                setarTextos(estado.quadrados);
                fieldGoto.setText(indice + "");
            } else {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro, tentando com outra ordem!", "Erro!", JOptionPane.ERROR_MESSAGE);
                botaoEmbaralharActionPerformed(evt);
                botaoResolverActionPerformed(evt);
            }
        }
        checarBotoes();

    }//GEN-LAST:event_botaoResolverActionPerformed
