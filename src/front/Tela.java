    private void botaoAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAnteriorActionPerformed
        // TODO add your handling code here:
        indice--;
        estado = estados.get(indice);
        setarTextos(estado.quadrados);
        checarBotoes();
        fieldGoto.setText(indice + "");

    }//GEN-LAST:event_botaoAnteriorActionPerformed
    public void checarBotoes() {
        botaoProximo(!(indice == estados.size() - 1));
        botaoAnterior(!(indice == 0));
        botaoFinal(!(indice>=estados.size()-1));
        botaoInicial(!(indice<=0));
        botaoGoto(!estados.isEmpty());
    }

    public void botoes(boolean b) {
        botaoAnterior(b);
        botaoProximo(b);
        botaoInicial(b);
        botaoFinal(b);
        botaoGoto(b);
    }

