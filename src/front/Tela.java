    public void checarPossibilidade() {
        if (estado.isObjetivo()) {
            botaoResolver.setEnabled(false);
            botaoResolver.setText("Resolvido");
        } else {
            boolean possivel = Algoritmos.isPossivel(estado);
            botaoResolver.setEnabled(possivel);
            botaoResolver.setText(possivel ? "Resolver" : "Impossível");
        }

    }

