    public byte[] randomizar() {
        List<Byte> valoresPossiveis = new ArrayList<>();
        for (Byte i = 0; i <= 8; i++) {
            valoresPossiveis.add(i);
        }
        byte[] vetor = new byte[9];
        SecureRandom random = new SecureRandom();

        for (Byte i = 0; i <= 8; i++) {
            int indice = random.nextInt(valoresPossiveis.size());
            vetor[i] = valoresPossiveis.get(indice);
            valoresPossiveis.remove(indice);
        }
        return vetor;
    }

